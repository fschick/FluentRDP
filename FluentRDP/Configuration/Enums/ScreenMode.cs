namespace FluentRDP.Configuration.Enums;

/// <summary>
/// Determines whether a remote session window appears full screen
/// </summary>
public enum ScreenMode
{

    /// <summary>
    /// A remote session appears in a window
    /// </summary>
    Windowed = 1,

    /// <summary>
    /// A remote session appears full screen
    /// </summary>
    FullScreen = 2
}